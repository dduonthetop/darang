GENERIC_ANSWER_REPLACEMENTS = [
    ("운영됨", "운영됩니다"),
    ("확인됨", "확인됩니다"),
    ("확인 가능", "확인하실 수 있습니다"),
    ("사용 가능", "사용 가능합니다"),
    ("발급 가능", "발급 가능합니다"),
    ("수령 가능", "수령 가능합니다"),
    ("제공 가능", "제공 가능합니다"),
    ("권장.", "권장드립니다."),
]


def normalize_question(text: str, faq_id: str) -> str:
    text = QUESTION_OVERRIDES.get(faq_id, (text or "").strip())
    for source, target in QUESTION_REPLACEMENTS:
        text = text.replace(source, target)
    return text


def normalize_answer(text: str, faq_id: str) -> str:
    text = ANSWER_OVERRIDES.get(faq_id, (text or "").strip())
    for source, target in GENERIC_ANSWER_REPLACEMENTS:
        text = text.replace(source, target)
    return text if text.endswith((".", "!", "?")) else f"{text}."


def normalize_next_action(text: str) -> str:
    text = NEXT_ACTION_OVERRIDES.get((text or "").strip(), (text or "").strip())
    if not text.endswith((".", "!", "?")):
        text = f"{text}."
    return text


def main() -> None:
    with CSV_PATH.open("r", encoding="utf-8-sig", newline="") as handle:
        rows = list(csv.DictReader(handle))
        headers = list(rows[0].keys()) if rows else []

    for row in rows:
        faq_id = row.get("faq_id", "").strip()
        row["question"] = normalize_question(row.get("question", ""), faq_id)
        row["answer"] = normalize_answer(row.get("answer", ""), faq_id)
        row["next_action"] = normalize_next_action(row.get("next_action", ""))

    with CSV_PATH.open("w", encoding="utf-8-sig", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=headers)
        writer.writeheader()
        writer.writerows(rows)

    _write_static_data(load_faq_items(CSV_PATH))


if __name__ == "__main__":
    main()
